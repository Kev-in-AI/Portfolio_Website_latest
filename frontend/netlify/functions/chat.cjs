const callNvidia = async (messages) => {
  const apiKey = process.env.NVIDIA_API_KEY || process.env.MISTRAL_API_KEY

  const response = await fetch('https://integrate.api.nvidia.com/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Accept: 'application/json',
      Authorization: `Bearer ${apiKey}`
    },
    body: JSON.stringify({
      model: process.env.NVIDIA_MODEL || 'mistralai/mistral-medium-3.5-128b',
      reasoning_effort: process.env.NVIDIA_REASONING_EFFORT || 'high',
      temperature: Number(process.env.NVIDIA_TEMPERATURE || 0.7),
      top_p: 1,
      max_tokens: Number(process.env.NVIDIA_MAX_TOKENS || 220),
      stream: false,
      messages: [
        { role: 'system', content: SYSTEM_PROMPT },
        ...messages
      ]
    })
  })

  if (!response.ok) {
    const errorText = await response.text()
    throw new Error(`NVIDIA request failed: ${response.status} ${errorText}`)
  }

  const data = await response.json()
  return data.choices?.[0]?.message?.content?.trim()
}

