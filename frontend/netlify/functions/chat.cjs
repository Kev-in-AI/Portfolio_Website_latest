const callGemini = async (messages) => {
  const apiKey = process.env.GEMINI_API_KEY
  const model = process.env.GEMINI_MODEL || 'gemini-1.5-flash'
  const conversation = messages
    .map((message) => `${message.role === 'user' ? 'User' : 'Gugu'}: ${message.content}`)
    .join('\n')

  const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent?key=${apiKey}`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      systemInstruction: {
        parts: [{ text: SYSTEM_PROMPT }]
      },
      contents: [
        {
          role: 'user',
          parts: [{ text: conversation }]
        }
      ],
      generationConfig: {
        temperature: 0.35,
        maxOutputTokens: 220
      }
    })
  })

  if (!response.ok) {
    const errorText = await response.text()
    throw new Error(`Gemini request failed: ${response.status} ${errorText}`)
  }

  const data = await response.json()
  return data.candidates?.[0]?.content?.parts?.map((part) => part.text).join('').trim()
}

exports.handler = async (event) => {
  if (event.httpMethod !== 'POST') {
    return jsonResponse(405, { error: 'Method not allowed' })
  }

  let payload
  try {
    payload = JSON.parse(event.body || '{}')
  } catch {
    return jsonResponse(400, { error: 'Invalid JSON body' })
  }

  const messages = normalizeMessages(payload.messages)
  const latestUserMessage = messages.filter((message) => message.role === 'user').at(-1)

  if (!latestUserMessage?.content?.trim()) {
    return jsonResponse(400, { error: 'A user message is required.' })
  }

  const directAnswer = getDirectPortfolioAnswer(latestUserMessage.content)
  if (directAnswer) {
    return jsonResponse(200, { reply: directAnswer })
  }

  try {
    let reply

    if (process.env.NVIDIA_API_KEY || process.env.MISTRAL_API_KEY) {
      reply = await callNvidia(messages)
    } else if (process.env.OPENAI_API_KEY) {
      reply = await callOpenAI(messages)
    } else if (process.env.GEMINI_API_KEY) {
      reply = await callGemini(messages)
    } else {
      return jsonResponse(500, {
        error: 'Gugu is not configured yet. Add NVIDIA_API_KEY, MISTRAL_API_KEY, OPENAI_API_KEY, or GEMINI_API_KEY in Netlify environment variables.'
      })
    }

    return jsonResponse(200, {
      reply: reply || 'I can help with Kevin’s profile, but I do not have that exact detail. Please contact Kevin directly!'
    })
  } catch (error) {
    console.error(error)
    return jsonResponse(500, {
      error: 'Gugu had trouble thinking for a second. Please try again soon.'
    })
  }
}
